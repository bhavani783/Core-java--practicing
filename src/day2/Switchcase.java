package day2;

public class Switchcase {
/*-->if we have no.of conditions we prefer to use switch case.
 * -->switch case will reduce the lines of code
 * -->the objective is same for nested if else and switch case,both will perform
 * same operations but switch case will reduce lines of code.
 */
	public static void main(String[] args) {
		int weeknumber=1;
		switch(weeknumber)
		{
		case 1:System.out.println("monday");
		break;
		case 2:System.out.println("tuesday");
		break;
		case 3:System.out.println("wednesday");
		break;
		case 4:System.out.println("thursday");
		break;
		case 5:System.out.println("friday");
		break;
		case 6:System.out.println("saturday");
		break;
		case 7:System.out.println("sunday");
		break;
		
		}
		

	}

}
