package day2;

public class Nestedifelse {
//if 1 condition can have multiple if conditions called nested if else.
	public static void main(String[] args) {
		int weeknumber=1;
		if(weeknumber==1) {
			System.out.println("monday"+weeknumber);
		}else if(weeknumber==2) {
			System.out.println("tuesday"+weeknumber);
		}else if(weeknumber==3) {
			System.out.println("wednesday"+weeknumber);
		}else if(weeknumber==4) {
			System.out.println("thursday"+weeknumber);
		}else if(weeknumber==5) {
			System.out.println("friday"+weeknumber);
		}else if(weeknumber==6) {
			System.out.println("saturday"+weeknumber);
		}else {
			System.out.println("sunday"+weeknumber);
		}

	}

}
